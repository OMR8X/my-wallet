import 'package:flutter/material.dart';

class RadiusHelper {
  static const double unit = 8.0;
  // radiuses
  static BorderRadius get r1 => BorderRadius.circular(unit);
  static BorderRadius get r2 => BorderRadius.circular(unit * 2);
  static BorderRadius get r3 => BorderRadius.circular(unit * 3);
  static BorderRadius get r4 => BorderRadius.circular(unit * 4);
  static BorderRadius get r5 => BorderRadius.circular(unit * 5);
}
