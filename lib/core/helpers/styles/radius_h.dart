import 'package:flutter/material.dart';

class RadiusHelper {
  static const double unit = 4.0;
  // radiuses
  BorderRadius get r1 => BorderRadius.circular(unit);
  BorderRadius get r2 => BorderRadius.circular(unit * 2);
  BorderRadius get r3 => BorderRadius.circular(unit * 3);
  BorderRadius get r4 => BorderRadius.circular(unit * 4);
  BorderRadius get r5 => BorderRadius.circular(unit * 5);
}
